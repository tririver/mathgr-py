def test_decorate_tex_string_matches_upstream_waste_and_time_dot_cleanup():
    import mathgr.typeset as typeset

    previous_template = typeset.ToTeXTemplate
    try:
        typeset.ToTeXTemplate = False

        assert typeset.DecorateTeXString(r"\partial_0\partial_0 f(x){}^2") == r"\ddot f(x)^2"
        assert typeset.DecorateTeXString(r"\partial^{-2}\left(x\right) + \partial_0 y") == (
            r"\partial^{-2}\left(x\right)"
            " \n +  "
            r"\partial_0 y"
        )
    finally:
        typeset.ToTeXTemplate = previous_template
