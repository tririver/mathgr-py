def test_typeset_renders_delta_indices_in_upstream_variance_order():
    from mathgr.tensor import Dta
    from mathgr.typeset import ToTeXString
    import mathgr.typeset as typeset

    previous_template = typeset.ToTeXTemplate

    try:
        typeset.ToTeXTemplate = False

        assert ToTeXString(Dta(UP("a"), DN("b"))) == r"\delta_{b}^{a}"
        assert ToTeXString(Dta(DN("b"), UP("a"))) == r"\delta_{b}^{a}"
        assert ToTeXString(Dta(UP("a"), UP("b"))) == r"\delta^{a}^{b}"
        assert ToTeXString(Dta(DN("a"), DN("b"))) == r"\delta_{a}_{b}"
    finally:
        typeset.ToTeXTemplate = previous_template


