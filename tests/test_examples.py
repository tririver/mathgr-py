def test_equilateral_example_ports_third_order_delta_phi_and_zeta_cells():
    from examples import equilateral

    results = equilateral.main()
    delta = equilateral.δφ(equilateral.x, equilateral.y, equilateral.z, equilateral.τ)
    δt = sp.diff(delta, equilateral.τ)
    delta_grad_sq = sum(sp.diff(delta, coord) ** 2 for coord in (equilateral.x, equilateral.y, equilateral.z))
    expected_delta_phi = (
        equilateral.a(equilateral.τ)
        * equilateral.φ0dot
        * (
            equilateral.PX**2
            * equilateral.λ
            * δt**3
            / (2 * equilateral.cs**4 * equilateral.Σ**2)
            + equilateral.PX**2
            * (equilateral.cs**2 - 1)
            * δt
            * delta_grad_sq
            / (4 * equilateral.cs**4 * equilateral.Σ)
        )
    )

    ζfield = equilateral.ζfield(equilateral.x, equilateral.y, equilateral.z, equilateral.τ)
    zeta_t = sp.diff(ζfield, equilateral.τ)
    zeta_grad_sq = sum(sp.diff(ζfield, coord) ** 2 for coord in (equilateral.x, equilateral.y, equilateral.z))
    expected_zeta = (
        -2 * equilateral.λ * equilateral.a(equilateral.τ) * zeta_t**3 / equilateral.H**3
        + equilateral.Σ
        * equilateral.a(equilateral.τ)
        * (1 - equilateral.cs**2)
        * zeta_t
        * zeta_grad_sq
        / equilateral.H**3
    )

    assert sp.simplify(results["lag3_delta_phi"] - expected_delta_phi) == 0
    assert sp.simplify(results["lag3_zeta"] - expected_zeta) == 0


def _implicit_index_label_counts(expr):
    counts = Counter()

    def visit(node, multiplier=1):
        node = sp.sympify(node)
        if isinstance(node, sp.Pow) and node.exp.is_Integer and node.exp > 0:
            visit(node.base, multiplier * int(node.exp))
            return
        if isinstance(node, Index) and node.head_name in {DN.name, UP.name}:
            counts[node.label] += multiplier
            return
        for arg in getattr(node, "args", ()):
            visit(arg, multiplier)

    visit(expr)
    return counts
