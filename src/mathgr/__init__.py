def __getattr__(name):
    if name in {"Metric", "IdxOfMetric"}:
        return getattr(_gr_module, name)
    if name == "SimpSelect":
        return getattr(_tensor_module, name)
    raise AttributeError(f"module {__name__!r} has no attribute {name!r}")
